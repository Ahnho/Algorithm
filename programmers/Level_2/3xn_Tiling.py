##                  3 x n 타일링                       ##
##########################################################

def solution(n):
    a,b = 0,1
    if n == 0 :
        return 1
    for i in range(2,n+1,2):
        a,b = a+b, b * 3 + a * 2
        
    return b % 1000000007
